def generate_name(gender: 'Gender') -> str:
    """Генерирует случайное имя для указанного пола"""
    from ..npc.character import Gender as GenderEnum

    if gender == GenderEnum.MALE:
        return random.choice(MALE_NAMES)
    else:
        return random.choice(FEMALE_NAMES)


def generate_surname(gender: 'Gender' = None) -> str:
    """Генерирует случайную фамилию"""
    from ..npc.character import Gender as GenderEnum

    surname = random.choice(SURNAMES)

    # Женские фамилии заканчиваются на "а"
    if gender == GenderEnum.FEMALE:
        if surname.endswith("ов") or surname.endswith("ев") or surname.endswith("ёв"):
            surname = surname + "а"
        elif surname.endswith("ий"):
            surname = surname[:-2] + "ая"

    return surname


def generate_full_name(gender: 'Gender') -> tuple:
    """Генерирует полное имя (имя + фамилия)"""
    return generate_name(gender), generate_surname(gender)
